#include <string>
using std::string;

class dependence {

		string name;
		int flits;
		
	public:
		
		dependence();
		dependence(string namex, int flits);
		string get_name();
		void set_name(string namex);
		int get_flits();
		void set_flits(long int flitsx);
};

dependence::dependence()
{
}
dependence::dependence(string namex, int flitsx)
{
	name = namex;
	flits = flitsx;
}
string dependence::get_name()
{ 
	return name;
}
void dependence::set_name(string namex)
{
	name = namex;
}
int dependence::get_flits()
{ 
	return flits;
}
void dependence::set_flits(long int flitsx)
{
	flits = flitsx;
}
